    private void jbtnLisaakasittelyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLisaakasittelyActionPerformed
        if (jListKasittelyt.getSelectedIndex() == -1) {
            return;
        }
        
        DefaultListModel mdl = (DefaultListModel)jListValitutKasittelyt.getModel();
        for (int i = 0; i < mdl.getSize(); i++) {
            if (mdl.get(i).toString().equals(jListKasittelyt.getSelectedValue())) {
                return;
            }
        }
        mdl.addElement(jListKasittelyt.getSelectedValue());
        paivitaHintaTeksti();
    }//GEN-LAST:event_jbtnLisaakasittelyActionPerformed

