    private String haeTilauksenNimi() {
        String nimi = jbtngrpAsiakasika.getSelection().getActionCommand();
        nimi += ", " + jbtngrpHiustenpituus.getSelection().getActionCommand();
        
        DefaultListModel mdl = (DefaultListModel)jListValitutKasittelyt.getModel();
        if (mdl.getSize() > 0) {
            nimi += " (";
        }
        for (int i = 0; i < mdl.getSize(); i++) {
            nimi += mdl.get(i).toString().split(",")[0];
            if (i != mdl.getSize()-1) {
                nimi += ", ";
            }
        }
        if (mdl.getSize() > 0) {
            nimi += ")";
        }
        return nimi;
    }
    private boolean tarkistaSyote() {
        try {
            int maara = Integer.valueOf(jtxtMaara.getText());
            if (maara <= 0 ) {
                JOptionPane.showInternalMessageDialog(null, "Määrä syötetty väärin!", "Virhe", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            double hinta = Double.valueOf(jtxtHinta.getText());
            if (hinta <= 0) {
                JOptionPane.showInternalMessageDialog(null, "Hinta syötetty väärin!", "Virhe", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(null, "Määrä tai hinta syötetty väärin!", "Virhe", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private void paivitaTilaustenSummaTeksti() {
        DefaultListModel mdl = (DefaultListModel)jListTilaukset.getModel();
        double summa = 0;
        for (int i = 0; i < tilaukset.haeTilauslistaPituus(); i++) {
            summa += tilaukset.haeTilaus(i).haeHintaSumma();
        }
        jtxtTilauksetYhteensa.setText(String.format("%.2f €", summa));
    }
    
    private void jbtnLisaatilausActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLisaatilausActionPerformed
        
        if (!tarkistaSyote()) {
            return;
        }
        tilaukset.lisaaTilaus(haeTilauksenNimi(), laskeHinta(), Integer.valueOf(jtxtMaara.getText()));
        
        DefaultListModel mdl = (DefaultListModel)jListTilaukset.getModel();
        mdl.clear();
        for (int i = 0; i < tilaukset.haeTilauslistaPituus(); i++) {
            Tilaus tl = tilaukset.haeTilaus(i);
            mdl.addElement(String.format("%s | Määrä: %d | A'-Hinta: %.2f € | Summa: %.2f €", tl.haeNimi(), tl.haeMaara(), tl.haeHinta(), tl.haeHintaSumma()));
        }
        paivitaTilaustenSummaTeksti();
    }//GEN-LAST:event_jbtnLisaatilausActionPerformed

    private void jbtnPoistaKaikkiTilauksetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPoistaKaikkiTilauksetActionPerformed
        
        if (JOptionPane.showConfirmDialog(null, "Haluatko varmasti poistaa kaikki tilaukset?", "Varmistus", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            DefaultListModel mdl = (DefaultListModel)jListTilaukset.getModel();
            mdl.clear();
            tilaukset.poistaKaikkiTilaukset();
            paivitaTilaustenSummaTeksti();
        }
    }//GEN-LAST:event_jbtnPoistaKaikkiTilauksetActionPerformed

    private double laskeHinta() {
       if (!tarkistaSyote()) {
           return 0;
       }
        
       double hinta = 0;
       String asiakasIka = jbtngrpAsiakasika.getSelection().getActionCommand();
       String hiustenPituus = jbtngrpHiustenpituus.getSelection().getActionCommand();
        switch (asiakasIka) {
            case "Aikuinen":
                hinta += 25;
                break;
            case "Lapsi":
                hinta += 15;
                break;
            default:
                throw new AssertionError();
        }
        switch (hiustenPituus) {
            case "Pitkät":
                hinta += 10;
                break;
            case "Keskipitkät":
                hinta += 5;
                break;
            case "Lyhyet":
                break;
            default:
                throw new AssertionError();
        }
        
        DefaultListModel mdl = (DefaultListModel)jListValitutKasittelyt.getModel();
        for (int i = 0; i < mdl.getSize(); i++) {
            String kasittely = mdl.get(i).toString().replace("€", "").split(",")[1];
            int kasittelyHinta = Integer.valueOf(kasittely);
            hinta += kasittelyHinta;
        }
        int maara = 1;
        try {
            maara = Integer.valueOf(jtxtMaara.getText());
        } catch (Exception e) {
        }
        
        if (hinta*maara >= 25) {
            if (jchkInternetvaraus.isSelected()) {
                hinta -= 5;
            }
        }
       return hinta;
    }
    
    private void paivitaHintaTeksti() {
        jtxtHinta.setText(String.valueOf(laskeHinta()));
    }
    
